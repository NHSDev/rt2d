GameScene::GameScene() : SuperScene()
{
	timer.start();

	canvas = new Canvas(4); // pixelsize
	layers[0]->addChild(canvas);

	setupTurret();
	setupPlayer();
	setupEnemyA();

	restart();
}

GameScene::~GameScene()
{
	layers[0]->removeChild(canvas);
	delete canvas;
}

void GameScene::update(float deltaTime)
{
	// ###############################################################
	// Let ss do what it needs to do ( Escape key stops the Scene )
	// ###############################################################
	SuperScene::update(deltaTime);

	float tsec = timer.seconds();
	if (tsec > 0.1 - deltaTime) {

		static float p = 0.0f;
		static PixelSprite plr = player;
		canvas->clearSprite(plr);
		plr = plr.rotated(p);
		plr.position = Pointi(canvas->width() / 2, 0);

		if (input()->getKey(KeyCode::Left)) {
			p -= HALF_PI / 46;
			if (p > TWO_PI) { p -= TWO_PI; }
			canvas->drawSprite(plr);
		}

		if (input()->getKey(KeyCode::Right)) {
			p += HALF_PI / 46;
			if (p > TWO_PI) { p -= TWO_PI; }
			canvas->drawSprite(plr);
		}
		canvas->drawSprite(plr);
		timer.start();
	}
}





void GameScene::restart() {
	canvas->fill(canvas->backgroundcolor);
	player.position.x = canvas->width() / 2;
	canvas->drawSprite(turret);
}


